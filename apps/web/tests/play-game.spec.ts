test.describe('cross-token attempt access', () => {
  test('attempt created with token A returns 404 when patched with token B', async ({
    page,
    request,
  }) => {
    await signInAsTutor(page, request);
    const a = await seedAssignedGame(page, { studentName: 'A', gameType: 'FILL_BLANK' });
    const b = await seedAssignedGame(page, { studentName: 'B', gameType: 'FILL_BLANK' });
    // Start an attempt as student A.
    const started = await request.post(
      `${API_BASE}/s/${a.shareToken}/games/${a.gameId}/attempts`,
      { data: {} },
    );
    expect(started.status()).toBe(201);
    const body = (await started.json()) as { attemptId: string; questions: { id: string }[] };

    // Try to PATCH that attempt via student B's share token.
    const r = await request.patch(`${API_BASE}/s/${b.shareToken}/attempts/${body.attemptId}/answers`, {
      data: { questionId: body.questions[0]!.id, rawAnswer: 'whatever' },
    });
    expect(r.status()).toBe(404);
  });
});
